package Model;

public class Requirement {
	private String subject;
	private int numHour;
	
	public void setSubject(String s) {
		subject = s;
	}
	
	public String getSubject() {
		return subject;
	}
	
	public void setNumHour(int n) {
		numHour = n;
	}
	
	public int getNumHour() {
		return numHour;
	}
}
